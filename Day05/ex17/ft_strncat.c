char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0')
	{
		if (i > nb - 1)
			dest[i] = '\0';
		dest[i] = src[j];
		i++;
		j++;
	}
	return (dest);
}
